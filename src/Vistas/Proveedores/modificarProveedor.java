    public void ObtenerProveedorModificar(Proveedores prov) {
        IdProv.setText("" + prov.getIdProvedor());
        nombreProveedor.setText(prov.getNombre());
        Emailtxt.setText(prov.getEmail());
        Telefonotxt.setText("" + prov.getTelefono());
        Coloniatxt.setText(prov.getColonia());
        Direcciontxt.setText(prov.getDireccion());
        CodigoPostxt.setText(prov.getCodPostall());

        if (prov.getStatus() == 'A') {
            this.estatus.setSelectedIndex(1);
        } else {
            this.estatus.setSelectedIndex(2);
        }
        LinkedList<Proveedores> listProv = new DAOs.DAOProveedoresImp().obtenerIdCiudad();
        DefaultComboBoxModel model = (DefaultComboBoxModel) idCiudad.getModel();
        for (int i = 0; i < listProv.size(); i++) {
            model.addElement(listProv.get(i).getIdCiudad());

        }
        idCiudad.setModel(model);
        for (int i = 0; i < listProv.size(); i++) {
            idCiudad.setSelectedIndex(i);
            if (idCiudad.getSelectedItem().toString().equals(prov.getIdCiudad())) {
                this.idCiudad.setSelectedIndex(i);
                break;
            }

        }
    }

