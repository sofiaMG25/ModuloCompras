    private void modificarUProMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_modificarUProMouseClicked
        if (nombreProveedor.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Ingrese un nombre válido", "Error", JOptionPane.ERROR_MESSAGE);
            nombreProveedor.requestFocus();
        }
        try {
            if (Emailtxt.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Ingrese un email valido", "Error", JOptionPane.ERROR_MESSAGE);
                Emailtxt.requestFocus();
            } else if (Telefonotxt.getText().equals("") || Float.parseFloat(Telefonotxt.getText()) <= 0) {
                JOptionPane.showMessageDialog(this, "Ingrese un numero de Telefono válido ", "Error", JOptionPane.ERROR_MESSAGE);
                Telefonotxt.requestFocus();
            } else if (Coloniatxt.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Ingrese una Colonia válida ", "Error", JOptionPane.ERROR_MESSAGE);
                Coloniatxt.requestFocus();
            } else if (Direcciontxt.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Ingrese una Dirección válida ", "Error", JOptionPane.ERROR_MESSAGE);
                Coloniatxt.requestFocus();  
            } else if (CodigoPostxt.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Ingrese un Codigo Postal valido ", "Error", JOptionPane.ERROR_MESSAGE);
                CodigoPostxt.requestFocus();
            } else if (idCiudad.getSelectedIndex() <= 0) {
                JOptionPane.showMessageDialog(this, "Elija una Ciudad Valida ", "Error", JOptionPane.ERROR_MESSAGE);
                idCiudad.requestFocus();
            
            } else {
                DAOProveedoresImp guardarProve = new DAOProveedoresImp();
                //Datos obtenidos de los campos
                int IdProveedor = Integer.parseInt(this.IdProv.getText());
                String nombre = this.nombreProveedor.getText();
                String Email = this.Emailtxt.getText();
                int Telefono = Integer.parseInt(this.Telefonotxt.getText());
                String Colonia = this.Coloniatxt.getText();
                String Direccion = this.Direcciontxt.getText();
                String CodigoPostal = this.CodigoPostxt.getText();
                char estatus = String.valueOf(this.estatus.getSelectedItem()).charAt(0);
                String Ciudad = this.idCiudad.getSelectedItem().toString();
                //Guardar los datos de laboratorio
                guardarProve.upadate(new Proveedores(IdProveedor, nombre, Email, Telefono, Direccion, Colonia, CodigoPostal, Ciudad, estatus));
                limpiarVariables();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }


    }//GEN-LAST:event_modificarUProMouseClicked

