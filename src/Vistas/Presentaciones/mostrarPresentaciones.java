    private void jTableMCMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMCMouseClicked
        int row;
        row = jTableMC.getSelectedRow();

        //Verificamos que el evento este en el rango de la tabla.
        if (row > -1 && row <= jTableMC.getSelectedRow()) {
            //creamos la ventana modificar
            modificarPresentaciones modificarPre = new modificarPresentaciones();
            this.mainPrincipal.getworkSpace().removeAll();
            modificarPre.setSize(mainPrincipal.getworkSpace().getSize());
            modificarPre.setVisible(true);
            modificarPre.setMainPrincipal(mainPrincipal);
            //Datos de la tabla, selecionar un row
            int id = Integer.parseInt(String.valueOf(this.jTableMC.getValueAt(row, 0)));
            float pCompra =Float.parseFloat(String.valueOf(this.jTableMC.getValueAt(row, 1)));
            float pVenta =Float.parseFloat(String.valueOf(this.jTableMC.getValueAt(row, 2)));
            float pReorden =Float.parseFloat(String.valueOf(this.jTableMC.getValueAt(row, 3)));
            String producto = String.valueOf(this.jTableMC.getValueAt(row, 4));
            String empaque = String.valueOf(this.jTableMC.getValueAt(row, 5));
            char estatus = String.valueOf(this.jTableMC.getValueAt(row, 6)).charAt(0);
            //Ingresan los datos de la tabla a la interfaz Modificar Laboratorio
            modificarPre.ObtenerPresentacionModificar(new Presentaciones(id, pCompra, pVenta, pReorden, producto, empaque, estatus));
                    

            this.mainPrincipal.getworkSpace().add(modificarPre, BorderLayout.CENTER);
            this.mainPrincipal.getworkSpace().revalidate();
            this.mainPrincipal.getworkSpace().repaint();
        }
    }//GEN-LAST:event_jTableMCMouseClicked

