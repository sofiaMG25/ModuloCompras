    private void guardarAEmpMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_guardarAEmpMouseClicked
        try {
            if (proveedorCbx.getSelectedIndex() <= 0) {
                JOptionPane.showMessageDialog(this, "Seleccione un proveedor\n, en caso de no tener uno, debe registrar un proveedor", "ERROR", JOptionPane.ERROR_MESSAGE);
                proveedorCbx.requestFocus();
            } else if (noCuentatxt.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Ingrese el numero de cuenta", "ERROR", JOptionPane.ERROR_MESSAGE);
                noCuentatxt.requestFocus();
            } else if (bancotxt.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Ingrese el nombre del banco", "ERROR", JOptionPane.ERROR_MESSAGE);
                bancotxt.requestFocus();
            } else {
                try {
                    new DAOCuentaProveedorImp().Insert(new CuentasProveedor(proveedorCbx.getSelectedItem().toString(),
                            noCuentatxt.getText(),
                            bancotxt.getText()));
                    limpiarVariables();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, " solo numeros", "Error de tipo de dato", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Capacidad solo numeros", "Error de tipo de dato", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_guardarAEmpMouseClicked

    private void cancelarAEmpMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cancelarAEmpMouseClicked
        mainPrincipal.getworkSpace().removeAll();
        mostrarCuentasProveedor mostrarCuentaPro = new mostrarCuentasProveedor();
        mostrarCuentaPro.setSize(mainPrincipal.getworkSpace().getSize());
        mostrarCuentaPro.setVisible(true);
        mostrarCuentaPro.setMostrarCuentasProveedor(mainPrincipal);
        mostrarCuentaPro.MostrarDatosCuentasProv();
        mainPrincipal.getworkSpace().add(mostrarCuentaPro, BorderLayout.CENTER);
        mainPrincipal.getworkSpace().revalidate();
        mainPrincipal.getworkSpace().repaint();

    }//GEN-LAST:event_cancelarAEmpMouseClicked

    private void limpiarVariables() {
        proveedorCbx.setSelectedIndex(0);
        noCuentatxt.setText("");
        bancotxt.setText("");

    }

