    private void mostrarProveedor() {
        LinkedList<CuentasProveedor> cProve = new DAOCuentaProveedorImp().obtenerIdProveedores();
        if (cProve != null) {
            DefaultComboBoxModel model = (DefaultComboBoxModel) proveedorCbx.getModel();
            for (int i = 0; i < cProve.size(); i++) {
                model.addElement(cProve.get(i).getProveedor());
            }
            proveedorCbx.setModel(model);
        }
    }

