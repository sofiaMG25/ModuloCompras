    private void jTableMImgProMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMImgProMouseClicked
        int row;
        row = jTableMImgPro.getSelectedRow();
        //Verificamos que el evento este en el rango de la tabla.
        if (row > -1 && row <= jTableMImgPro.getSelectedRow()) {
            //creamos la ventana modificar
            modificarImagenesProducto modificarEmpaque = new modificarImagenesProducto();
            this.mainPrincipal.getworkSpace().removeAll();
            modificarEmpaque.setSize(mainPrincipal.getworkSpace().getSize());
            modificarEmpaque.setVisible(true);
            modificarEmpaque.setMainPrincipal(mainPrincipal);
            //Datos de la tabla, selecionar un row

            int id = Integer.parseInt(String.valueOf(this.jTableMImgPro.getValueAt(row, 0)));
            String nombImg = String.valueOf(this.jTableMImgPro.getValueAt(row, 1));
            byte[] imagen = (String.valueOf(this.jTableMImgPro.getValueAt(row, 2))).getBytes();
            char principal = String.valueOf(this.jTableMImgPro.getValueAt(row, 3)).charAt(0);
            char estatus = String.valueOf(this.jTableMImgPro.getValueAt(row, 4)).charAt(0);
            String producto = (String.valueOf(this.jTableMImgPro.getValueAt(row, 5)));
            //Ingresan los datos de la tabla a la interfaz Modificar Laboratorio
            modificarEmpaque.ObtenerLaboratoriModificar(new ImagenesProducto(id, nombImg, 
                        imagen, principal, estatus, producto));

            this.mainPrincipal.getworkSpace().add(modificarEmpaque, BorderLayout.CENTER);
            this.mainPrincipal.getworkSpace().revalidate();
            this.mainPrincipal.getworkSpace().repaint();
        }
    }//GEN-LAST:event_jTableMImgProMouseClicked

