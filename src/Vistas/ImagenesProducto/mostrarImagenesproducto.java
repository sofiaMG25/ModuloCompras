    public void MostrarDatosPedidoDetalle() {
        LinkedList<ImagenesProducto> emp = new DAOImagenesProductoImp().show(cambioPagina);
        if (ContRegistro <= 0) {
            next.setVisible(false);
            preview.setVisible(false);
            return;
        } else {
            next.setVisible(true);
            preview.setVisible(true);
            this.segmentacion.setText("Página " + cambioPagina + " de " + (int) ContRegistro + " Páginas en total");
            Object listaDatos[][] = new Object[emp.size()][10];
            for (int i = 0; i < emp.size(); i++) {
                listaDatos[i][0] = emp.get(i).getIdImagen();
                listaDatos[i][1] = emp.get(i).getNombreImagen();
                listaDatos[i][2] = emp.get(i).getImagen();
                listaDatos[i][3] = emp.get(i).getPrincipal();
                listaDatos[i][4] = emp.get(i).getEstatus();
                listaDatos[i][5] = emp.get(i).getIdProducto();
            }

            DefaultTableModel modelTable = new DefaultTableModel(
                    listaDatos,
                    new Object[]{"ID", "NOMBIMAGEN", "IMAGEN",
                        "PRINCIPAL", "ESTATUS", "IDPRODUCTO"}) {
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };

            this.jTableMImgPro.setModel(modelTable);
            this.jTableMImgPro.getColumnModel().getColumn(0).setMinWidth(10);
            this.jTableMImgPro.getColumnModel().getColumn(0).setMaxWidth(50);

            this.jTableMImgPro.getColumnModel().getColumn(1).setMinWidth(50);
            this.jTableMImgPro.getColumnModel().getColumn(1).setMaxWidth(350);

            this.jTableMImgPro.getColumnModel().getColumn(2).setMinWidth(50);
            this.jTableMImgPro.getColumnModel().getColumn(2).setMaxWidth(350);

            this.jTableMImgPro.getColumnModel().getColumn(3).setMinWidth(10);
            this.jTableMImgPro.getColumnModel().getColumn(3).setMaxWidth(200);

            this.jTableMImgPro.getColumnModel().getColumn(4).setMinWidth(10);
            this.jTableMImgPro.getColumnModel().getColumn(4).setMaxWidth(200);

            this.jTableMImgPro.getColumnModel().getColumn(5).setMinWidth(10);
            this.jTableMImgPro.getColumnModel().getColumn(5).setMaxWidth(200);


        }
    }

